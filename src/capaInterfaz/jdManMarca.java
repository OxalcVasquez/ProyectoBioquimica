    public Boolean validarCampos() {
        if (txtNombre.getText().isEmpty() || txtCodigo.getText().isEmpty()) {
            new MensajeMed().mostrar(this, "Por favor complete todos los campos", 0);
            return false;
        }
        return true;
    }

    public void listarMarcas() {
        ResultSet rsListaMarcas = null;
        DefaultTableModel modelMarca = new DefaultTableModel() {
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        modelMarca.addColumn("Cod. Marca");
        modelMarca.addColumn("Nombre");
        modelMarca.addColumn("Vigencia");
        tblDatos.setModel(modelMarca);

        try {
            rsListaMarcas = objMarca.listarMarcas();
            while (rsListaMarcas.next()) {

                modelMarca.addRow(new Object[]{
                    rsListaMarcas.getInt("codMarca"),
                    rsListaMarcas.getString("nombre"),
                    rsListaMarcas.getBoolean("vigencia") ? "Vigente" : "No vigente"

                });

            }
        } catch (Exception e) {
            new MensajeMed().mostrar(this, e.getMessage(), 3);
        }

    }
