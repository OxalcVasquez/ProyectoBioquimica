    private String obtenerTipoComprobante() {

        String tipo[] = cboTipo.getSelectedItem().toString().split(" ");
        if (tipo.length > 1) {
            return (tipo[0].substring(0, 1) + tipo[1].substring(0, 1));
        } else {
            return tipo[0].substring(0, 1);

        }
    }
