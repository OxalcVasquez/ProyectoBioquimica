    private void listarTrabajador(){
    
        ResultSet rs = null;
        String cargo = "";
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Codigo");
        modelo.addColumn("Nombres");
        modelo.addColumn("Apellidos");
        modelo.addColumn("Dni");
        modelo.addColumn("Telefono");
        modelo.addColumn("Correo");
        modelo.addColumn("Cargo");
        modelo.addColumn("Vigencia");
        
        tblTrabajador.setModel(modelo);
        
        try {
            rs = objTra.listarTrabajador();
            while(rs.next()){
                if(rs.getString("cargo").equals("C")) cargo = "Cajero";
                if(rs.getString("cargo").equals("V")) cargo = "Vendedor";
                if(rs.getString("cargo").equals("A")) cargo = "Administrador";
                if(rs.getString("cargo").equals("Y")) cargo = "Apoyo";
                modelo.addRow(new Object[]{rs.getString("codtrabajador"),rs.getString("nombres"),rs.getString("apellidos"),rs.getString("dni"),rs.getString("telefono"),rs.getString("correo"),cargo,rs.getString("vigencia") });
            }
            alinearTabla(tblTrabajador);
        } catch (Exception e) {
       JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
    }
    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        Character cargo = null ;
        Boolean sexo  = null;
        try {
            if(btnNuevo.getText().equals("Nuevo")){
                btnNuevo.setText("Guardar");
                txtCodigo.setText(String.valueOf(objTra.codigoTrabajadorPA()));
                txtCodigo.enable(false);
                txtNombre.requestFocus(); 
            }else{
                btnNuevo.setText("Nuevo");
                if(cboSexo.getSelectedItem().equals("Masculino")) sexo = true;
                if(cboSexo.getSelectedItem().equals("Femenino")) sexo = false;
                if(cboCargo.getSelectedItem().equals("Caja")) cargo = 'C';
                if(cboCargo.getSelectedItem().equals("Vendedor")) cargo = 'V';
                if(cboCargo.getSelectedItem().equals("Administrador")) cargo = 'A';
                if(cboCargo.getSelectedItem().equals("Apoyo")) cargo = 'Y';
                
                String fecha = new SimpleDateFormat("yyyy/MM/dd").format(jdCalendar.getDate()); 
               
                
                objTra.registrarTrabajador(Integer.parseInt(txtCodigo.getText()),txtNombre.getText(),txtApellido.getText(),txtDNI.getText(),txtTelefono.getText(),txtCorreo.getText(), txtDireccion.getText(),sexo,fecha,cargo,chkEstado.isSelected());
                new MensajeMed().mostrar(this,"Trabajador Registrado correctamente",2);
                limpiarContenido();
                listarTrabajador(); 
            }
        } catch (Exception e) {
        
             new MensajeMed().mostrar(this, e.getMessage(),3);
        }
  
    }//GEN-LAST:event_btnNuevoActionPerformed

