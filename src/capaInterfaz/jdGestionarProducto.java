    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
        try {
            if (btnNuevo.getText().equals("Nuevo")) {
                btnNuevo.setText("Guardar");
                txtCodigo.setText(String.valueOf(objProducto.generarCodigoProducto()));

            } else {
                btnNuevo.setText("Nuevo");
                //Guardar

                if (validarCampos()) {
                    objProducto.registrarProducto(Integer.parseInt(txtCodigo.getText()),
                            txtNombre.getText(),
                            txtDescripcion.getText(),
                            Double.parseDouble(txtPVenta.getText()),
                            Double.parseDouble(txtPCompra.getText()),
                            Integer.parseInt(txtStock.getText()),
                            chkVigencia.isSelected(),
                            sdf.format(jdcVencimiento.getDate()),
                            objMarca.obtenerCodigoMarca(cboMarca.getSelectedItem().toString()),
                            objCat.obtenerCodigoCategoria(cboCategoria.getSelectedItem().toString()));
                }

                listarProductos();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnNuevoActionPerformed
    public Boolean validarCampos() {
        if (txtNombre.getText().isEmpty() || txtCodigo.getText().isEmpty() || txtDescripcion.getText().isEmpty()
                || txtPCompra.getText().isEmpty() || txtPVenta.getText().isEmpty() || txtStock.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor complete todos los campos");
            return false;
        }
        return true;
    }

    public void listarProductos() {
        ResultSet rsLista = null;
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Codigo");
        model.addColumn("Nombre");
        model.addColumn("Descripcion");
        model.addColumn("P. Venta");
        model.addColumn("P. Compra");
        model.addColumn("Stock");
        model.addColumn("Vigencia");
        model.addColumn("F. Vencimiento");
        model.addColumn("Marca");
        model.addColumn("Categoria");
        tblDatos.setModel(model);
        try {
            rsLista = objProducto.listarProductos();
            while (rsLista.next()) {

                model.addRow(new Object[]{
                    rsLista.getInt("codproducto"),
                    rsLista.getString("nombre"),
                    rsLista.getString("descripcion"),
                    rsLista.getDouble("precioventa"),
                    rsLista.getDouble("preciocompra"),
                    rsLista.getInt("stock"),
                    rsLista.getBoolean("vigencia") ? "Vigente" : "No Vigente",
                    rsLista.getDate("fechavencimiento"),
                    rsLista.getString("marca"),
                    rsLista.getString("categoria")
                });

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

