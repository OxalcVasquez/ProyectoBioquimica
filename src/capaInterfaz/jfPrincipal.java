    private void botones(Integer num) {

        switch (num) {

            case 0: // Mi usuario
                lblUsuario.setBackground(new Color(0, 204, 106));

                lblReportes.setBackground(new Color(255, 255, 255));
                lblOperaciones.setBackground(new Color(255, 255, 255));
                lblConsultas.setBackground(new Color(255, 255, 255));
                lblAcercaDe.setBackground(new Color(255, 255, 255));
                lblMantenimiento.setBackground(new Color(255, 255, 255));
                break;

            case 1: // Mantenimiento
                lblMantenimiento.setBackground(new Color(0, 204, 106));

                lblReportes.setBackground(new Color(255, 255, 255));
                lblOperaciones.setBackground(new Color(255, 255, 255));
                lblConsultas.setBackground(new Color(255, 255, 255));
                lblAcercaDe.setBackground(new Color(255, 255, 255));
                lblUsuario.setBackground(new Color(255, 255, 255));
                break;

            case 2: // Operaciones
                lblOperaciones.setBackground(new Color(0, 204, 106));

                lblMantenimiento.setBackground(new Color(255, 255, 255));
                lblConsultas.setBackground(new Color(255, 255, 255));
                lblReportes.setBackground(new Color(255, 255, 255));
                lblAcercaDe.setBackground(new Color(255, 255, 255));
                lblUsuario.setBackground(new Color(255, 255, 255));
                break;

            case 3: // Consultas
                lblConsultas.setBackground(new Color(0, 204, 106));

                lblMantenimiento.setBackground(new Color(255, 255, 255));
                lblOperaciones.setBackground(new Color(255, 255, 255));
                lblReportes.setBackground(new Color(255, 255, 255));
                lblAcercaDe.setBackground(new Color(255, 255, 255));
                lblUsuario.setBackground(new Color(255, 255, 255));
                break;

            case 4: // Reportes
                lblReportes.setBackground(new Color(0, 204, 106));

                lblMantenimiento.setBackground(new Color(255, 255, 255));
                lblOperaciones.setBackground(new Color(255, 255, 255));
                lblConsultas.setBackground(new Color(255, 255, 255));
                lblAcercaDe.setBackground(new Color(255, 255, 255));
                lblUsuario.setBackground(new Color(255, 255, 255));
                break;

            case 5: // Acerca de
                lblAcercaDe.setBackground(new Color(0, 204, 106));

                lblReportes.setBackground(new Color(255, 255, 255));
                lblOperaciones.setBackground(new Color(255, 255, 255));
                lblConsultas.setBackground(new Color(255, 255, 255));
                lblMantenimiento.setBackground(new Color(255, 255, 255));
                lblUsuario.setBackground(new Color(255, 255, 255));
                break;

        }

    }

