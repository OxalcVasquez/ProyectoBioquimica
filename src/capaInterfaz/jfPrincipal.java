    clsUsuario miUsuario = new clsUsuario();
    Preferences preferencia;
    boolean recordarPreferencia;

    private void iniciarSesion() throws Exception {
        preferencia = Preferences.userNodeForPackage(this.getClass());
        // Put the boolean of the rememberMe preferencia
        recordarPreferencia = preferencia.getBoolean("rememberMe", Boolean.valueOf(""));
        // Check if the check box was selected
        if (recordarPreferencia) {
            // Replace the textField by the preferencia User and Password who will be stock
            String[] usuario = miUsuario.inicioSesion(preferencia.get("User", ""), preferencia.get("Password", "").toCharArray());

            imagen(lblImagen, "src/Iconos/" + usuario[0] + ".png");
            lblCodigo.setText(usuario[1]);
            lblCargo.setText(usuario[2]);
            lblNombre.setText(usuario[3]);
            lblNombreUsuario.setText(usuario[4]);
            lblDNI.setText(usuario[5]);
            lblFechaNacimiento.setText(usuario[6]);
            lblTelefono.setText(usuario[7]);
            lblCorreo.setText(usuario[8]);
            lblDireccion.setText(usuario[9]);
            lblFechaRegistro.setText(usuario[10]);

        } else {
            this.setVisible(false);
            jdInicioSesion sesion = new jdInicioSesion(this, true);
            sesion.setVisible(true);
            this.setVisible(true);
            imagen(lblImagen, "src/Iconos/" + sesion.usuario[0] + ".png");
            lblCodigo.setText(sesion.usuario[1]);
            lblCargo.setText(sesion.usuario[2]);
            lblNombre.setText(sesion.usuario[3]);
            lblNombreUsuario.setText(sesion.usuario[4]);
            lblDNI.setText(sesion.usuario[5]);
            lblFechaNacimiento.setText(sesion.usuario[6]);
            lblTelefono.setText(sesion.usuario[7]);
            lblCorreo.setText(sesion.usuario[8]);
            lblDireccion.setText(sesion.usuario[9]);
            lblFechaRegistro.setText(sesion.usuario[10]);
        }

    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int respuesta = new MensajeMed().mostrar(this, "¿Está seguro que desea cerrar sesión?", 1);
        if (respuesta == JOptionPane.YES_OPTION) {
            preferencia.put("User", "");
            preferencia.put("Password", "");
            preferencia.putBoolean("rememberMe", false);
            try {
                iniciarSesion();
            } catch (Exception ex) {
                Logger.getLogger(jfPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void imagen(JLabel lbl, String ruta) {
        ImageIcon imagen = new ImageIcon(ruta);
        Icon icono = new ImageIcon(imagen.getImage().getScaledInstance(lbl.getWidth(), lbl.getHeight(), Image.SCALE_SMOOTH));

        lbl.setIcon(icono);
        this.repaint();

    }

